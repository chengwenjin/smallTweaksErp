SELECT 
    '班组数据统计' as info,
    COUNT(*) as total_count,
    SUM(CASE WHEN group_type = '1' THEN 1 ELSE 0 END) as 生产班数量,
    SUM(CASE WHEN group_type = '2' THEN 1 ELSE 0 END) as 维修班数量,
    SUM(CASE WHEN group_type = '3' THEN 1 ELSE 0 END) as 质检班数量
FROM erp_work_group 
WHERE is_deleted = 0;

